package defaultpackage;

public class Switch {
 static int day = 2;
	
	
	public static void main(String[] args) {
   
   switch (day) {
	case 1:
		System.out.println("Saturday");
	    break;
	  
	case 2:
	    System.out.println("Sunday");   
	break	;
	

default:
    System.out.println("Weekend");
	}
	
	
	}}
	
	
	
	
	
